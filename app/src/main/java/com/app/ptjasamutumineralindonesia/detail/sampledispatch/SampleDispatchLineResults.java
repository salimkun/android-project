package com.app.ptjasamutumineralindonesia.detail.sampledispatch;

public class SampleDispatchLineResults {
    private String bagNumber;
    private String dispatch;
    private String dispatchType;
    private String id;
    private String received;
    private String sampleDispatchId;
    private String sealNumber;

    public String getBagNumber() {
        return bagNumber;
    }

    public void setBagNumber(String bagNumber) {
        this.bagNumber = bagNumber;
    }

    public String getDispatch() {
        return dispatch;
    }

    public void setDispatch(String dispatch) {
        this.dispatch = dispatch;
    }

    public String getDispatchType() {
        return dispatchType;
    }

    public void setDispatchType(String dispatchType) {
        this.dispatchType = dispatchType;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getReceived() {
        return received;
    }

    public void setReceived(String received) {
        this.received = received;
    }

    public String getSampleDispatchId() {
        return sampleDispatchId;
    }

    public void setSampleDispatchId(String sampleDispatchId) {
        this.sampleDispatchId = sampleDispatchId;
    }

    public String getSealNumber() {
        return sealNumber;
    }

    public void setSealNumber(String sealNumber) {
        this.sealNumber = sealNumber;
    }
}
