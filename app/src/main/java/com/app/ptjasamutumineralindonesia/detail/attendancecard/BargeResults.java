package com.app.ptjasamutumineralindonesia.detail.attendancecard;

public class BargeResults {
    private String id;
    private String name;
    private String description;
    private String shipTypeId;
    private String coompanyId;
    private String shipTypeName;
    private String companyName;


    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getShipTypeId() {
        return shipTypeId;
    }

    public void setShipTypeId(String shipTypeId) {
        this.shipTypeId = shipTypeId;
    }

    public String getCoompanyId() {
        return coompanyId;
    }

    public void setCoompanyId(String coompanyId) {
        this.coompanyId = coompanyId;
    }

    public String getShipTypeName() {
        return shipTypeName;
    }

    public void setShipTypeName(String shipTypeName) {
        this.shipTypeName = shipTypeName;
    }

    public String getCompanyName() {
        return companyName;
    }

    public void setCompanyName(String companyName) {
        this.companyName = companyName;
    }
}
